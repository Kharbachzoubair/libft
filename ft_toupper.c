#include <stdio.h>
int ft_toupper(int c)
{
    if (c >= 97 && c <= 122)
    {
        return (c - 32);
    }
    return c;
}
int main ()
{
    char c1 = '3';
     printf("'%c' est '%c'\n", c1, ft_toupper(c1));
    return 0;
}